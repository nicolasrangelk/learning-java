package Condicionais;

public class IfElse {
    public static void Executar() {
        int idade = 24;
        if (idade >= 18) {
            System.out.println("Maior");
        } else if (idade == 30) {
            System.out.println("Ficando tiozinho");
        } else {
            System.out.println("Menor");
        }
    }

}
